int		ft_putchar(char c);

void	ft_print_comb(void)
{
	int n[3];

	while (n[0] <= 7)
	{
		n[1] = 1;
		while (n[1] <= 8)
		{
			n[2] = 2;
			while (n[2] <= 9)
			{
				if (n[0] < n[1] && n[1] < n[2])
				{
					ft_putchar(n[0] + 48);
					ft_putchar(n[1] + 48);
					ft_putchar(n[2] + 48);
					n[0] != 7 || n[1] != 8 || n[2] != 9 ? ft_putchar(',') : 42;
					n[0] != 7 || n[1] != 8 || n[2] != 9 ? ft_putchar(' ') : 42;
				}
				n[2]++;
			}
			n[1]++;
		}
		n[0]++;
	}
}
