int		ft_putchar(char c);

void	ft_print_comb2(void)
{
	int n[2];

	while (n[0] < 99)
	{
		n[1] = 1;
		while (n[1] <= 99)
		{
			if (n[0] < n[1])
			{
				ft_putchar(n[0] / 10 + 48);
				ft_putchar(n[0] % 10 + 48);
				ft_putchar(' ');
				ft_putchar(n[1] / 10 + 48);
				ft_putchar(n[1] % 10 + 48);
				if (n[0] != 98 || n[1] != 99)
				{
					ft_putchar(',');
					ft_putchar(' ');
				}
			}
			n[1]++;
		}
		n[0]++;
	}
}
