char	*ft_strcat(char dest[], char *src)
{
	int i;
	int n;

	n = 0;
	i = 0;
	while (dest[i])
		i++;
	while (n - 1 == -1 || src[n - 1])
	{
		dest[i + n] = src[n];
		n++;
	}
	return (dest);
}
