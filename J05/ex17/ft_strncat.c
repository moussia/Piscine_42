char	*ft_strncat(char *dest, char *src, int nb)
{
	int i;
	int n;

	n = 0;
	i = 0;
	while (dest[i])
		i++;
	while ((n - 1 == -1 || src[n - 1]) && nb > n)
	{
		dest[i + n] = src[n];
		n++;
	}
	if (n == nb)
		dest[i + n] = '\0';
	return (dest);
}
